
public class OperatorSample2 {

	public static void main(String[] args) {
		
		int x = 1;
		System.out.println(x);
		
		x++;
		System.out.println(x);
		
		x+=10;
		System.out.println(x);
		
		
		
		System.out.println("-------");
		
		
		
		x = 100;
		System.out.println(x);
		
		x--;
		System.out.println(x);
		
		x-=10;
		System.out.println(x);
		
		
		
		System.out.println("-------");
		
		
		
		x = 2;
		System.out.println(x);
		
		x*=2;
		System.out.println(x);
		
		x*=10;
		System.out.println(x);

		
		
		System.out.println("-------");
		
		
		
		x = 32;
		System.out.println(x);
		
		x/=2;
		System.out.println(x);
		
		x/=4;
		System.out.println(x);
		
		
		
		System.out.println("-------");
		
		
		
		x = 99;
		System.out.println(x);
		
		x%=10;
		System.out.println(x);
		
	}

}
